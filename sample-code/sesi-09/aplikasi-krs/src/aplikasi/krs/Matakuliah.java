package aplikasi.krs;

public class Matakuliah {
    private Integer sks;
    private String kode;
    private String nama;

    public Integer getSks(){
        return sks;
    }

    public void setSks(Integer s){
        this.sks = s;
    }

    public String getKode(){
        return kode;
    }

    public void setKode(String s){
        this.kode = s;
    }

    public String getNama(){
        return nama;
    }

    public void setNama(String s){
        this.nama = s;
    }
}
